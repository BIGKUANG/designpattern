package strategypattern;
/**
 * 
 * 在策略模式中,一个类的的行为或者其算法在运行时更改,这种类型的的设计模式属于行为模式.
 * 在策略模式中,我们创建表示各种策略的对象和一个行为随着策略对象改变而改变的context对象.策略对象改变context对象
 * 如果一个对象有很多行为,不使用恰当的模式,这些行为就只好使用多重的条件选择语句来实现
 * 的执行算法
 * 使用场景:
 * 1.如果一个系统里面有许多类,它们之间的区别仅在于他们的行为,那么使用策略模式可以动态的让一个对象在许多行为中选择一种行为
 * 2.一个系统需要动态的的在击中算法中选择是一种
 * 
 * 
 * @author Dekey.Shen
 *
 */
public interface Strategy {
	public int doOperation(int num1,int num2);
}
